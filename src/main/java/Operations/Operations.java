package Operations;

public class Operations {

    public static int Sum(int a, int b) {
        return a + b;
    }

    public static float Sub(float a, float b) {
        return a - b;
    }

    public static float Multiplication(float a, float b) {
        return a * b;
    }

    public static float Division(float a, float b) {
        return a/b;
    }

}
